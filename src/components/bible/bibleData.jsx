export function generateChapterId(bookIndex, chapter) {
  const book = BIBLE_BOOKS[bookIndex];
  if (!book) return null;
  const paddedIndex = String(bookIndex + 1).padStart(2, '0');
  const paddedChapter = String(chapter).padStart(3, '0');
  return `${paddedIndex}-${book.abbrev}-${paddedChapter}`;
}